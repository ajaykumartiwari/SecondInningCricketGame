public class Batsmen {

	String batsmen;
	
	public Batsmen() {

	}

	public String getBatsman() {
		return batsmen;
	}

	public void setBatsman(String batsman) {
		this.batsmen = batsman;
	}

	@Override
	public String toString() {
		return "Batsman [batsman=" + batsmen + "]";
	}
	
	

}
